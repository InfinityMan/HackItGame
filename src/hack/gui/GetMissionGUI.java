    public void setList() {
        String[] a = new String[user.availableContracts.size()];
        for (int i = 0; i < user.availableContracts.size(); i++) {
            Contract get = user.availableContracts.get(i);
            a[i] = get.missionShort;
        }
        jList1.setListData(a);
    }
    
    public void update() {
        setList();
    }

